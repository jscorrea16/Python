# Definite Loops
# Quite often we have a list of items
# of the lines in a file - effectively
# a finite set of things. These are
# called "definite loops" because
# they execute an exact number of times.
for i in [5, 4, 3, 2, 1] :
    print(i)
print("Blastoff!")

# A Definite Loop with Strings
friends = ["Joseph", "Glen", "Sally"]
for friend in friends :
    print("Happy New Year:", friend)
print("Done!")

